    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        String prName1 = name.getText();
        int amount = 0;
        try {
            amount = Integer.parseInt(amount1.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Amount is a number");
        }
        if(products.size()==0){
            JOptionPane.showMessageDialog(rootPane, "Please Add The Product List!");
        }
        for(int i = 0 ; i < products.size();i++){
            if(prName1.equals(products.get(i).getProductName())){
                if((products.get(i).getAmount()-amount)>0)
                    products.set(i,new Product(prName1,products.get(i).getAmount()-amount,products.get(i).getDescription()));
                else{
                    products.remove(i);
                }
            }
        }
        tblModel.setRowCount(0);
        for(int i = 0 ; i < products.size();i++){
            tblModel.addRow(new Object[]{i+1,products.get(i).getProductName(),products.get(i).getAmount()});
             }        
            
                     
                   
                    
                        
      
        
    }                                        

    private void searchActionPerformed(java.awt.event.ActionEvent evt) {                                       
        String prName2 = name1.getText();
        
        for(int i = 0 ; i < products.size();i++){
            if(prName2.equals(products.get(i).getProductName())){
                amount2.setText(String.valueOf(products.get(i).getAmount()));
                des1.setText(products.get(i).getDescription());
               
                }
        }
        ResetForm();
                
                
                
           
                
    }                                      

       
    

