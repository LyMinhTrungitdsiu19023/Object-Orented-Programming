    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        int amount = 0;
        boolean isOK = true ;
        boolean isUpdate = false;
       
        String prName = name.getText();
        String Des = des.getText();
        
        
        try {
            amount = Integer.parseInt(amount1.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Amount is a number");
            
            isOK = false;
        }
        for(int i = 0 ; i < products.size();i++){
            if(prName.equals(products.get(i).getProductName()))
                isUpdate = true;
        }
        if(isOK== true&&isUpdate == false){
            Product p1 = new Product(prName,amount,Des);
            addProduct(p1);
            status.setText("SUCCESSFUL ADDING");
            try {
                Thread.sleep(200);
            } catch (InterruptedException ex) {
                Logger.getLogger(Frame1.class.getName()).log(Level.SEVERE, null, ex);
            }
           
            ResetForm();
        }
        if(isOK = true&& isUpdate == true){
            for(int i = 0 ; i < products.size();i++){
                if(prName.equals(products.get(i).getProductName())){
                    products.set(i,new Product(prName,products.get(i).getAmount()+amount,Des));
                }
            }
            tblModel.setRowCount(0);// xoa tat ca cac dong trong bang hien tai
            for(int i = 0 ; i < products.size();i++){
                 tblModel.addRow(new Object[]{i+1,products.get(i).getProductName(),products.get(i).getAmount()});
             }        
                    }

     
        
     
        
    }                                        
    
